    private void CrearModelo(){
        MPartido dataPartido = new MPartido();
        modelo.clear();
        for(String item : dataPartido.Listar()){
            modelo.addElement(item);
        }
        Lista.setModel(modelo);
        ListaModificar.setModel(modelo);
        ListaEliminar.setModel(modelo);
    }
