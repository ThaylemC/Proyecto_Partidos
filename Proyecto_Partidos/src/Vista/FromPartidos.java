    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        String ELocal = (String) EquipoLocal.getSelectedItem();
        String EVisitante = (String) EquipoVisitante.getSelectedItem();
        int GLocal = (int) GolesLocal.getValue();
        int GVisitante = (int) GolesVisitante.getValue();

        if(ValidarCampos(GLocal,GVisitante,ELocal,EVisitante)){

            Partido partido = new Partido(ELocal,GLocal,EVisitante,GVisitante);
            MPartido dataPartido = new MPartido(partido);

            if(dataPartido.Agregar()){
                JOptionPane.showMessageDialog(null,"Se agregó");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Hay valores incorrectos.");
        }
    }//GEN-LAST:event_btnAgregarMouseClicked

    private boolean ValidarCampos(int GLocal,int GVisitante, String ELocal, String EVisitante){
        if(GLocal >= 0 || GVisitante >= 0){
            if(!ELocal.equals("") && !ELocal.equals("")){
                if(!ELocal.equals(EVisitante)){
                    return true;
                }
            }
        }
        return false;
    }
    private void ListaModificarValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ListaModificarValueChanged
        // TODO add your handling code here:
        if(ListaModificar.getSelectedValue() != null ){
            CargarModeloVisitante(EquipoLocal1);
            CargarModeloLocal(EquipoVisitante1);
            String[] Datos = ListaModificar.getSelectedValue().split("\\|");
            EquipoLocal1.setSelectedItem(Datos[1]);
            EquipoVisitante1.setSelectedItem(Datos[3]);
            txtID.setText(Datos[0]);
            GolesLocal1.setValue(Integer.parseInt(Datos[2]));
            GolesVisitante1.setValue(Integer.parseInt(Datos[4]));
        }
    }//GEN-LAST:event_ListaModificarValueChanged

    private void GuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GuardarMouseClicked
        // TODO add your handling code here:
        try{
            String ELocal = (String) EquipoLocal1.getSelectedItem();
            String EVisitante = (String) EquipoVisitante1.getSelectedItem();
            int GLocal = (int) GolesLocal1.getValue();
            int GVisitante = (int) GolesVisitante1.getValue();
            int ID = Integer.parseInt(txtID.getText());
            if(ValidarCampos(GLocal,GVisitante,ELocal,EVisitante)){
                Partido partido = new Partido();
                partido.setID(ID);
                partido.setEquipo_Local(ELocal);
                partido.setGoles_Local(GLocal);

                partido.setEquipo_Visitante(EVisitante);
                partido.setGoles_Visitante(GVisitante);
                partido.setFecha(LocalDate.now());


                MPartido dataPartido = new MPartido(partido);
                if(dataPartido.Actualizar()){
                    JOptionPane.showMessageDialog(null, "Se actualizó correctamente");
                }
                CrearModelo();        
            }else{
                JOptionPane.showMessageDialog(null,"Hay datos incorrectos.");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Hay valores incorrectos.");
        }
    }//GEN-LAST:event_GuardarMouseClicked

