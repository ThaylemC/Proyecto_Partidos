    private void CargarModeloLocal(JComboBox equipo){
        DefaultComboBoxModel<String> modeloLocal = new DefaultComboBoxModel<>();
        try (BufferedReader br = new BufferedReader(new FileReader(Equipos))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                String[] Datos = linea.split("\\|");
                modeloLocal.addElement(Datos[1]);
            }
            br.close();
            equipo.setModel(modeloLocal);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }
