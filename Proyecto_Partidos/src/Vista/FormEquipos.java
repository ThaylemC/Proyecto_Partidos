    private void CrearModelo(){
        MEquipo dataEquipo = new MEquipo();
        modelo.clear();
        for(String item : dataEquipo.Listar()){
            modelo.addElement(item);
        }
        Lista.setModel(modelo);
        ListaModificar.setModel(modelo);
        ListaEliminar.setModel(modelo);
    }
    private void ListaModificarValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ListaModificarValueChanged
        // TODO add your handling code here:
        
        if(ListaModificar.getSelectedValue() != null ){
            String[] Datos = ListaModificar.getSelectedValue().split("\\|");
            txtID.setText(Datos[0]);
            txtNombreEquipo1.setText(Datos[1]);
            txtNombreEntrenador1.setText(Datos[2]);
        }
    }//GEN-LAST:event_ListaModificarValueChanged

    private void GuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GuardarMouseClicked
        // TODO add your handling code here:
        String NEquipo = txtNombreEquipo1.getText();
        String NEntrenador = txtNombreEntrenador1.getText();
        if(ValidarCampos(NEquipo,NEntrenador)){
            Equipo equipo = new Equipo();
            equipo.setID(Integer.parseInt(txtID.getText()));
            equipo.setNombre_Equipo(NEquipo);
            equipo.setNombre_Entrenador(NEntrenador);
            MEquipo dataEquipo = new MEquipo(equipo);
            if(dataEquipo.Actualizar()){
                JOptionPane.showMessageDialog(null, "Se actualizó correctamente");
            }
            CrearModelo();    
        }else{
            JOptionPane.showMessageDialog(null,"Hay datos erroneos");
        }
    }//GEN-LAST:event_GuardarMouseClicked

