    private void Eliminar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Eliminar1MouseClicked
        // TODO add your handling code here:
        if(ListaEliminar.getSelectedValue() != null){
            int indice = ListaEliminar.getSelectedIndex();
            
            if(indice != -1){
                String elemento = ListaEliminar.getSelectedValue();
                Equipo equipo = new Equipo();
                equipo.setID(Integer.parseInt(txtID1.getText()));
                MEquipo dataEquipo = new MEquipo(equipo);
                if(dataEquipo.Eliminar()){
                    JOptionPane.showMessageDialog(null,"Se eliminó");
                    modelo.removeElement(elemento);
                    VaciarCampos();
                }
            }
        }
    }//GEN-LAST:event_Eliminar1MouseClicked

