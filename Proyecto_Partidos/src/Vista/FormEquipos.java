    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        String NEquipo = txtNombreEquipo.getText();
        String NEntrenador = txtNombreEntrenador.getText();
        if(ValidarCampos(NEquipo,NEntrenador)){
            Equipo equipo = new Equipo(NEquipo,NEntrenador );
            MEquipo dataEquipo = new MEquipo(equipo);
            if(dataEquipo.Agregar()){
                JOptionPane.showMessageDialog(null,"Se Agregó correctamente.");                   
                VaciarCampos();
            }      
        }else{
            JOptionPane.showMessageDialog(null,"Hay datos erroneos");
        }
      
    }//GEN-LAST:event_btnAgregarMouseClicked
    private boolean ValidarCampos(String NEquipo, String NEntrenador){
        if(!NEquipo.equals("") && !NEntrenador.equals("")){
            return true;
        }
        return false;
    }
